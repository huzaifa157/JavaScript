
// 🟢 JavaScript Data Types

// JavaScript has two categories of data types:

// 1️⃣ Primitive Data Types (basic, single values)

// String → "Hello", 'World'
// Number → 10, 3.14
// Boolean → true, false
// Null → special value meaning empty / nothing
// Undefined → a variable that is declared but not assigned a value
// Symbol → unique value (used for object properties)
// BigInt → very large integers

// 2️⃣ Non-Primitive (Reference) Data Types

// Object → { name: "Huzaifa",
//            age: 21 }
// Array → [1, 2, 3, 4]
// Function → function hello() { return "Hi"; }


